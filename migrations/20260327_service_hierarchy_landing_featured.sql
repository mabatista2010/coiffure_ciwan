-- Jerarquia de servicios + destacados landing

create table if not exists public.service_groups (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null,
  description text,
  image_url text,
  is_visible boolean not null default true,
  skip_subgroup_step_when_single_visible_child boolean not null default false,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.service_subgroups (
  id bigint generated by default as identity primary key,
  group_id bigint not null references public.service_groups(id) on delete restrict,
  name text not null,
  slug text not null,
  description text,
  is_visible boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.service_groups enable row level security;
alter table public.service_subgroups enable row level security;

alter table public.servicios
  add column if not exists slug text,
  add column if not exists group_id bigint,
  add column if not exists subgroup_id bigint,
  add column if not exists sort_order integer not null default 0,
  add column if not exists landing_featured boolean not null default false,
  add column if not exists landing_sort_order integer,
  add column if not exists updated_at timestamptz not null default now();

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.servicios'::regclass
      and conname = 'servicios_group_id_fkey'
  ) then
    alter table public.servicios
      add constraint servicios_group_id_fkey
      foreign key (group_id) references public.service_groups(id) on delete restrict;
  end if;

  if not exists (
    select 1 from pg_constraint
    where conrelid = 'public.servicios'::regclass
      and conname = 'servicios_subgroup_id_fkey'
  ) then
    alter table public.servicios
      add constraint servicios_subgroup_id_fkey
      foreign key (subgroup_id) references public.service_subgroups(id) on delete restrict;
  end if;
end$$;

create or replace function public.slugify_catalog_text(value text)
returns text
language sql
immutable
as $$
  select trim(both '-' from regexp_replace(
    lower(
      translate(
        coalesce(value, ''),
        'áàäâãåéèëêíìïîóòöôõúùüûñçÁÀÄÂÃÅÉÈËÊÍÌÏÎÓÒÖÔÕÚÙÜÛÑÇ',
        'aaaaaaeeeeiiiiooooouuuuncAAAAAAEEEEIIIIOOOOOUUUUNC'
      )
    ),
    '[^a-z0-9]+',
    '-',
    'g'
  ));
$$;

create or replace function public.current_service_group_mode(p_group_id bigint)
returns text
language sql
stable
as $$
  with has_direct as (
    select exists(
      select 1
      from public.servicios s
      where s.group_id = p_group_id
        and s.subgroup_id is null
    ) as value
  ),
  has_subgroups as (
    select exists(
      select 1
      from public.service_subgroups sg
      where sg.group_id = p_group_id
    ) as value
  )
  select case
    when (select value from has_direct) then 'services'
    when (select value from has_subgroups) then 'subgroups'
    else 'empty'
  end;
$$;

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

create or replace function public.service_groups_before_write()
returns trigger
language plpgsql
as $$
declare
  v_slug text;
begin
  if new.name is null or btrim(new.name) = '' then
    raise exception 'Le nom du groupe est obligatoire';
  end if;

  if new.slug is null or btrim(new.slug) = '' then
    v_slug := public.slugify_catalog_text(new.name);
    if v_slug = '' then
      v_slug := 'groupe';
    end if;
    new.slug := v_slug;
  else
    new.slug := public.slugify_catalog_text(new.slug);
  end if;

  new.sort_order := coalesce(new.sort_order, 0);
  new.is_visible := coalesce(new.is_visible, true);
  new.skip_subgroup_step_when_single_visible_child := coalesce(new.skip_subgroup_step_when_single_visible_child, false);

  return new;
end;
$$;

create or replace function public.service_subgroups_before_write()
returns trigger
language plpgsql
as $$
declare
  v_slug text;
begin
  if new.name is null or btrim(new.name) = '' then
    raise exception 'Le nom du sous-groupe est obligatoire';
  end if;

  if new.group_id is null then
    raise exception 'Le groupe parent est obligatoire';
  end if;

  if public.current_service_group_mode(new.group_id) = 'services' then
    raise exception 'Ce groupe contient deja des services directs et ne peut pas recevoir de sous-groupes';
  end if;

  if new.slug is null or btrim(new.slug) = '' then
    v_slug := public.slugify_catalog_text(new.name);
    if v_slug = '' then
      v_slug := 'sous-groupe';
    end if;
    new.slug := v_slug;
  else
    new.slug := public.slugify_catalog_text(new.slug);
  end if;

  new.sort_order := coalesce(new.sort_order, 0);
  new.is_visible := coalesce(new.is_visible, true);

  return new;
end;
$$;

create or replace function public.servicios_before_write_hierarchy()
returns trigger
language plpgsql
as $$
declare
  v_slug text;
  v_subgroup_group_id bigint;
  v_existing_featured_count integer;
begin
  if new.nombre is null or btrim(new.nombre) = '' then
    raise exception 'Le nom du service est obligatoire';
  end if;

  if new.subgroup_id is not null then
    select group_id into v_subgroup_group_id
    from public.service_subgroups
    where id = new.subgroup_id;

    if v_subgroup_group_id is null then
      raise exception 'Le sous-groupe selectionne est introuvable';
    end if;

    new.group_id := v_subgroup_group_id;
  end if;

  if new.group_id is null then
    raise exception 'Le groupe parent est obligatoire';
  end if;

  if new.subgroup_id is null and public.current_service_group_mode(new.group_id) = 'subgroups' then
    raise exception 'Ce groupe contient deja des sous-groupes et ne peut pas recevoir de services directs';
  end if;

  if new.slug is null or btrim(new.slug) = '' then
    v_slug := public.slugify_catalog_text(new.nombre);
    if v_slug = '' then
      v_slug := 'service';
    end if;
    new.slug := v_slug;
  else
    new.slug := public.slugify_catalog_text(new.slug);
  end if;

  new.sort_order := coalesce(new.sort_order, 0);
  new.landing_featured := coalesce(new.landing_featured, false);

  if new.landing_featured then
    select count(*)::integer
      into v_existing_featured_count
    from public.servicios s
    where s.landing_featured = true
      and (tg_op = 'INSERT' or s.id <> new.id);

    if v_existing_featured_count >= 6 then
      raise exception 'Maximum 6 services mis en avant sur la landing';
    end if;
  end if;

  if not new.landing_featured then
    new.landing_sort_order := null;
  else
    new.landing_sort_order := coalesce(new.landing_sort_order, 0);
  end if;

  return new;
end;
$$;

