with inserted_group as (
  insert into public.service_groups (name, slug, description, is_visible, sort_order)
  select 'Services', 'services', 'Groupe initial cree automatiquement pendant la migration', true, 0
  where not exists (select 1 from public.service_groups)
  returning id
), ensured_group as (
  select id from inserted_group
  union all
  select id from public.service_groups order by id limit 1
)
insert into public.service_subgroups (group_id, name, slug, description, is_visible, sort_order)
select id, 'General', 'general', 'Sous-groupe initial cree automatiquement pendant la migration', true, 0
from ensured_group
where not exists (select 1 from public.service_subgroups);

with default_subgroup as (
  select sg.id, sg.group_id
  from public.service_subgroups sg
  order by sg.sort_order, sg.id
  limit 1
), ordered_services as (
  select id, row_number() over (order by id) - 1 as desired_order
  from public.servicios
)
update public.servicios s
set group_id = coalesce(s.group_id, ds.group_id),
    subgroup_id = coalesce(s.subgroup_id, ds.id),
    sort_order = os.desired_order,
    landing_featured = coalesce(s.landing_featured, false),
    updated_at = now()
from default_subgroup ds, ordered_services os
where os.id = s.id
  and (s.group_id is null
   or s.subgroup_id is null
   or s.sort_order is null);

with ordered_groups as (
  select id,
         public.slugify_catalog_text(name) as base_slug,
         row_number() over (partition by public.slugify_catalog_text(name) order by id) as dup_rank
  from public.service_groups
)
update public.service_groups g
set slug = case when og.dup_rank = 1 then og.base_slug else og.base_slug || '-' || og.dup_rank end
from ordered_groups og
where g.id = og.id
  and (g.slug is null or btrim(g.slug) = '');

with ordered_subgroups as (
  select id,
         public.slugify_catalog_text(name) as base_slug,
         row_number() over (partition by public.slugify_catalog_text(name) order by id) as dup_rank
  from public.service_subgroups
)
update public.service_subgroups sg
set slug = case when os.dup_rank = 1 then os.base_slug else os.base_slug || '-' || os.dup_rank end
from ordered_subgroups os
where sg.id = os.id
  and (sg.slug is null or btrim(sg.slug) = '');

with ordered_services as (
  select id,
         public.slugify_catalog_text(nombre) as base_slug,
         row_number() over (partition by public.slugify_catalog_text(nombre) order by id) as dup_rank
  from public.servicios
)
update public.servicios s
set slug = case when os.dup_rank = 1 then os.base_slug else os.base_slug || '-' || os.dup_rank end,
    landing_featured = coalesce(s.landing_featured, false),
    updated_at = now()
from ordered_services os
where s.id = os.id
  and (s.slug is null or btrim(s.slug) = '');

update public.service_groups set sort_order = coalesce(sort_order, 0);
update public.service_subgroups set sort_order = coalesce(sort_order, 0);
update public.servicios set sort_order = coalesce(sort_order, 0), landing_featured = coalesce(landing_featured, false);

alter table public.servicios
  alter column slug set not null,
  alter column group_id set not null,
  alter column landing_featured set not null,
  alter column sort_order set not null;

create unique index if not exists service_groups_slug_key_idx
  on public.service_groups (slug);
create unique index if not exists service_subgroups_slug_key_idx
  on public.service_subgroups (slug);
create unique index if not exists servicios_slug_key_idx
  on public.servicios (slug);

create unique index if not exists service_groups_name_unique_idx
  on public.service_groups (lower(name));
create unique index if not exists service_subgroups_group_name_unique_idx
  on public.service_subgroups (group_id, lower(name));
create unique index if not exists servicios_direct_group_name_unique_idx
  on public.servicios (group_id, lower(nombre))
  where subgroup_id is null;
create unique index if not exists servicios_subgroup_name_unique_idx
  on public.servicios (subgroup_id, lower(nombre))
  where subgroup_id is not null;

create index if not exists service_groups_sort_order_idx on public.service_groups (sort_order, id);
create index if not exists service_subgroups_group_sort_order_idx on public.service_subgroups (group_id, sort_order, id);
create index if not exists servicios_group_sort_order_idx on public.servicios (group_id, subgroup_id, sort_order, id);
create index if not exists servicios_landing_featured_sort_idx on public.servicios (landing_featured, landing_sort_order, sort_order, id);

